  fmtTime: function(mins) {
    var h = Math.floor(mins / 60) % 24;
    var m = mins % 60;
    var ap = h >= 12 ? "PM" : "AM";
    var h12 = h > 12 ? h - 12 : (h === 0 ? 12 : h);
    return h12 + ":" + (m < 10 ? "0" + m : m) + " " + ap;
  },

